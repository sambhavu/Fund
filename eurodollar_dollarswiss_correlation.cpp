double trading::correlation(vector<double> d1, vector<double> d2)
{
    double meanx=0, meany=0; 
    int xsize, ysize; 
    
    xsize = d1.size();
    ysize = d2.size();
    
    for(int i = 0; i<xsize; i++)
    {   
     meanx +=d1[i];
    }
    
    meanx =meanx/xsize; 
    
    for(int i = 0; i< ysize; i++)
    {
     meany +=d2[i];   
    }
    
    meany = meany/ysize; 
    
    
    double sum = 0; 
    double sigmax;
    double sigmay;
    
    for(int i = 0; i < xsize; i++)
    {
        sum += (d1[i]-meanx)*(d1[i]-meanx);
    } 
    
    sigmax = sqrt(sum/(xsize-1));
    
    
    sum=0;
    
    for(int i =0; i<ysize; i++)
    {
        sum +=(d2[i]-meany)*(d2[i]-meany);   
    }
    
    sigmay=sqrt(sum/(ysize-1));
    

    
    sum=0; 
    double x, y; 
    
    for(int i = 0; i<xsize; i++)
    {
        x= d1[i] - meanx;
        y= d2[i] - meany;
        
        sum +=x*y; 
        
        
    }
    
    double cor; 
    cor = (sum/(sigmax*sigmay)) * (1/((double)xsize-1));
    return cor; 
    
} 


